# PEP 8 Style guide
# packages: short, all-lowercase names, and preferably no underscores
# modules: short, all-lowercase name, and can have underscores
# Classes: CapWords (upper camel case) convention
# Functions: lowercase, words separated by underscores (snake_case)
# Variables: lowercase, words separated by underscores (snake_case)
# Constants: all-uppercase, words are separated by underscores.